    gboolean    long_mnc;

    long_mnc = FALSE;
    start_offset = offset;

    /* MCC digits 1 */
    octet = tvb_get_guint8(tvb,offset);
    mcc1  = octet >> 4;
    offset++;

    /* MCC digits 1 and 2 */
    octet = tvb_get_guint8(tvb,offset);
    mcc2  = octet & 0x0f;
    mcc3  = octet >> 4;
    offset++;

    /* MNC digit 1 and MNC digit 2 */
    octet = tvb_get_guint8(tvb,offset);
    mnc1  = octet & 0x0f;
    mnc2  = octet >> 4;
    offset++;

    /* MNC digits 3 */
    octet = tvb_get_guint8(tvb,offset);
    mnc3  = octet & 0x0f;

    mcc   = 100 * mcc1 + 10 * mcc2 + mcc3;
    mnc   = 10 * mnc1 + mnc2;

